public class Looping {
	public static void main(String[]  cfg)
	{	
		
		int arr[] = {2,56,56,43,65};		
		int count = 0;
		
	for(int  i:arr)
	{
		System.out.println(i);
		count++;
	}
	
     System.out.println(System.lineSeparator());
	
	System.out.println(count);
	
	}

}
